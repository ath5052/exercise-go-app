class Exercise {
  int _id;
  String _title;
  String _description;
  String _date;
  String _exerciseId;

  Exercise(this._title, this._date, [this._description]);

  Exercise.withId(this._id, this._title, this._date, [this._description]);

  int get id => _id;

  String get title => _title;

  String get description => _description;

  String get date => _date;

  String get exerciseId => _exerciseId;

  set title(String newTitle) {
    if (newTitle.length <= 255) {
      this._title = newTitle;
    }
  }

  set description(String newDescription) {
    if (newDescription.length <= 255) {
      this._description = newDescription;
    }
  }

  set date(String newDate) {
    this._date = newDate;
  }

  set exerciseId(String id) {
    this._exerciseId = id;
  }

  Map<String, dynamic> toMap() {
    var map = Map<String, dynamic>();
    if (id != null) {
      map['id'] = _id;
    }
    map['title'] = _title;
    map['description'] = _description;
    map['date'] = _date;
    map['exerciseId'] = _exerciseId;

    return map;
  }

  Exercise.fromMapObject(Map<String, dynamic> map) {
    this._id = map['id'];
    this._title = map['title'];
    this._description = map['description'];
    this._date = map['date'];
    this._exerciseId = map['exerciseId'];
  }
}
